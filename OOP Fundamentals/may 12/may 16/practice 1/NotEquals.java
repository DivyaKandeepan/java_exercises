

public class NotEquals {
    public static void main(String[] args) {
        Integer input1 = 100;
        Integer input2 = 200;

        if (input1.equals(input2)) {
            System.out.println("input1 == input2");
        } else {
            System.out.println("input1 != input2");
            System.out.println("input1 = "+input1);
        }
    }
}
